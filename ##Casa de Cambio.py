##Casa de Cambio

##Ingresar el tipo de cambio del dólar Vendedor
dolar = float(input("Ingrese el tipo de cambio del dólar vendedor en pesos: "))

##Ingresa el monto en pesos a convertir a dolar

pesos = float(input("Ingrese el monto en pesos a convertir: "))

##Convertir pesos a dolar

dolar_pesos = pesos / dolar

##Mostrar resultado 

print("El monto en dolar es: ", dolar_pesos)

